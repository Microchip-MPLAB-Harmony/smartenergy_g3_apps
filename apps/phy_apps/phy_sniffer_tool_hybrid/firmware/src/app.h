/* Application states

  Summary:
    Application states enumeration

  Description:
    This enumeration defines the valid application states.  These states
    determine the behavior of the application at various times.
*/

typedef enum
{
    /* Application's state machine's initial state. */
    APP_STATE_INIT = 0,
    APP_STATE_REGISTER_PLC,
    APP_STATE_REGISTER_RF,
    APP_STATE_CONFIG_USI,
    APP_STATE_READY,
    APP_STATE_ERROR

} APP_STATES;


// *****************************************************************************
/* Application Data

  Summary:
    Holds application data

  Description:
    This structure holds the application's data.

  Remarks:
    Application strings and buffers are be defined outside this structure.
 */

typedef struct
{
    /* The application's current state */
    APP_STATES state;

    SYS_TIME_HANDLE tmr1Handle;

    volatile bool tmr1Expired;

    SYS_TIME_HANDLE tmr2Handle;

    volatile bool tmr2Expired;

    DRV_HANDLE drvPlcHandle;

    DRV_HANDLE drvRf215Handle;

    SRV_USI_HANDLE srvUSIHandle;

    DRV_PLC_PHY_PIB_OBJ plcPIB;

} APP_DATA;

